  <p class="intro">
  Quickies are designed as a high intensity, short duration workout that can serve as a warm up, 
  or when performed in quantity, a workout in themselves.  Each of our &ldquo;quickies&rdquo; 
  target a specific area of your body, as specified in their description.  Individually, they 
  are not that difficult, but are intended to be done in groups of two or more different 
  quickies, or in sets of two or more of the same quickie, depending on time or fitness level.  
  Each quickie should take between 2 &frac12; to 5 minutes, depending on your fitness level, with 
  equal recovery in between sets.  If any of them prove too difficult, you should attempt to 
  take a little more rest in between or slightly lower the reps of each exercise.  Remember 
  that these are designed to be high-intensity.  You should push yourself to complete them and 
  feel some burn after their completion.
